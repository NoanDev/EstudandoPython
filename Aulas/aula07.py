n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 + n2
multiplic = n1 * n2
divis = n1 / n2
divisinteira = n1 // n2
exponenc = n1 ** n2
print('A soma é {}, o produto é {} e a divisão é {:.3f}'.format(soma, multiplic, divis))
print('Divisão inteira {} e potência {}'.format(divisinteira, exponenc))
